#!/usr/bin/env python3
"""
   Defines a module that implements a function  to_str that takes
   a float n as argument and returns the string representation of the float
"""


def to_str(n: float) -> str:
    """
    Args:
        n (float): The first number.

    Returns:
        str: The str representation of `n`.
    """
    return str(n)
